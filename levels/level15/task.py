def main():
    # Eingabe der ersten Zahl
    erste_zahl = int(input("Geben Sie die erste Zahl ein: "))
    # Eingabe der zweiten Zahl
    zweite_zahl = int(input("Geben Sie die zweite Zahl ein: "))

    # Berechnung der Summe
    summe = erste_zahl + zweite_zahl

    # Ausgabe der Summe
    print(f"Die Summe ist: {summe}")

if __name__ == "__main__":
    main()
